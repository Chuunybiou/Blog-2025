    <h2 id="section-6">La SIM locale et la connexion mobile de secours</h2>
    <p>Même dans les meilleurs coworkings, une coupure Wi-Fi peut arriver. Avoir une SIM vietnamienne avec un forfait data 4G est indispensable pour tout nomade sérieux. Les trois opérateurs à considérer :</p>
    <ul>
      <li><strong>Viettel</strong> : meilleure couverture nationale, signal excellent même en dehors de Hanoï. Forfait données : 30 000 à 70 000 VND/mois pour données illimitées selon la formule.</li>
      <li><strong>Vietnamobile / Gmobile</strong> : opérateurs low-cost, moins couverts mais très économiques pour les séjours courts.</li>
      <li><strong>Vinaphone (VNPT)</strong> : bon réseau urbain, qualité stable, souvent distribué à l'aéroport dès l'arrivée.</li>
    </ul>
    <p>S'obtient facilement dans n'importe quelle boutique d'opérateur ou dans les supermarchés VinMart avec passeport ou carte de résidence. Le numéro vietnamien est aussi utile pour s'inscrire sur des services locaux (MoMo, Grab, ZaloPay).</p>
    <div class="tip-box">
      <strong>Le partage de connexion d'urgence :</strong>
      Configure ton téléphone en point d'accès mobile avant chaque session importante. Une coupure de 10 minutes peut suffire à perturber un appel client. La 4G vietnamienne (30–50 Mb/s en moyenne à Hanoï) suffit largement pour le télétravail.
    </div>

    <h2 id="section-7">Équipement à emporter pour télétravailler confortablement</h2>
    <p>Le coworking vietnamien ne ressemble pas toujours à un bureau parisien. Voici l'équipement qui fait vraiment la différence :</p>
    <ul>
      <li><strong>Casque à réduction de bruit</strong> : indispensable dans les espaces ouverts et les cafés. Sony WH-1000XM5 ou Bose QC45 sont les références des nomades à Hanoï.</li>
      <li><strong>Multiprise avec protection surtension</strong> : les prises sont parfois rares, et les micro-coupures de courant peuvent griller un chargeur. Une multiprise à 3 ou 4 prises et une entrée USB est un atout.</li>
      <li><strong>Adaptateur universel</strong> : la norme vietnamienne (type A et C) est compatible avec les prises françaises type E/F, mais un adaptateur compact évite les mauvaises surprises dans certains espaces anciens.</li>
      <li><strong>Ventilateur personnel portable</strong> : entre avril et octobre, même les espaces climatisés peuvent avoir des zones chaudes. Un petit ventilateur USB de bureau coûte 80 000–150 000 VND.</li>
      <li><strong>Support ordinateur portable</strong> : pour éviter les problèmes de dos lors des longues sessions. Les ergonomes recommandent la hauteur des yeux au niveau de l'écran — indispensable en déplacement.</li>
    </ul>
    <div class="info-box">
      <strong>La saison des pluies (juin–octobre) :</strong>
      Les orages tropicaux peuvent provoquer des micro-coupures de courant et des chutes de débit soudaines. Sauvegarde ton travail fréquemment et configure un système de sauvegarde automatique dans le cloud (Google Drive, Dropbox, iCloud). Évite de laisser des tâches critiques sans filet de sauvegarde durant les grosses pluies.
    </div>

