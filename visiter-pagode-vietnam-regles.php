    <h2 id="section-6">Ce que j'ai observé à Hanoï et à Ninh Binh</h2>

    <p>À Hanoï, les pagodes sont souvent intégrées dans la vie de quartier. On y croise des femmes âgées qui viennent prier tous les matins, des familles qui font des offrandes, des enfants. Ce n'est pas un site touristique — c'est un lieu vivant.</p>

    <p>À Ninh Binh, la grande pagode que nous avons visitée était impressionnante par sa taille. La foule y était plus mixte — touristes vietnamiens et étrangers. Mais les règles s'appliquaient de la même façon. C'est là que ma femme, en jupe, a décidé de rester à l'extérieur pendant que j'entrais. Elle n'avait pas l'air frustrée — c'était évident pour elle.</p>

    <p>Ce que j'ai retenu : les pagodes n'ont généralement pas de "police des tenues" à l'entrée. C'est la conscience individuelle et le respect qui priment. Ma femme me l'a dit clairement : <em>"On n'a pas besoin qu'on te dise ce qui est respectueux. On le ressent."</em></p>

