    <h2 id="section-pieges">Les pièges classiques à éviter</h2>

    <ul>
      <li><strong>Le "prix touriste" non négocié</strong> — Les propriétaires affichent souvent 10 à 20% au-dessus du prix réel pour les étrangers. Toujours contre-proposer, même sur les plateformes en ligne. Un engagement sur 12 mois justifie facilement une réduction de 5 à 10%.</li>
      <li><strong>La caution sans reçu</strong> — Exigez un reçu écrit signé pour chaque paiement. En cas de litige sur la restitution de la caution, c'est votre seule preuve.</li>
      <li><strong>Le compteur électrique partagé</strong> — Certains immeubles ont un compteur principal pour tout le bâtiment, et le propriétaire répartit la facture. Vous n'avez alors aucun contrôle sur votre consommation réelle. Préférez un appartement avec compteur individuel.</li>
      <li><strong>Le bail sans clause de résiliation</strong> — Si votre visa n'est pas renouvelé ou que vous devez repartir, un bail sans clause de résiliation anticipée peut vous coûter plusieurs mois de loyer. Négociez toujours une sortie à 30 ou 60 jours de préavis.</li>
      <li><strong>Les photos trompeuses</strong> — Photos retouchées, grand angle extrême, meubles retirés après la signature. Visitez en personne, prenez vos propres photos lors de l'état des lieux d'entrée.</li>
      <li><strong>L'augmentation de loyer non encadrée</strong> — Certains propriétaires augmentent le loyer au renouvellement de 15 à 30%. Négociez dès la signature un plafond d'augmentation annuelle inscrit dans le contrat.</li>
    </ul>

    <div class="tip-box">
      <strong>💡 Astuce : loger en guesthouse les 2 premières semaines</strong>
      Ne cherchez pas à tout régler depuis la France. Réservez une chambre dans une guesthouse bien placée (Tây Hồ ou Hoàn Kiếm) pour vos deux premières semaines. Arrivez, prenez le temps de visiter les quartiers à scooter, rejoignez les groupes Facebook locaux, et signez votre contrat une fois que vous connaissez la ville. C'est systématiquement plus efficace et moins risqué.
    </div>

