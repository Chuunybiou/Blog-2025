    <h2 id="section-chercher">Où et comment chercher un appartement</h2>

    <h3>Les plateformes en ligne</h3>
    <ul>
      <li><strong>Batdongsan.com.vn</strong> — le plus grand portail immobilier vietnamien. Interface en vietnamien mais les annonces expat sont souvent en anglais. Filtre par arrondissement (<em>quận</em>), type de bien et prix.</li>
      <li><strong>Homebase Vietnam</strong> — plateforme orientée expats, interface en anglais, bonne sélection à Tây Hồ. Tarifs légèrement au-dessus du marché mais listings fiables.</li>
      <li><strong>Airbnb / Booking</strong> — utile pour les premiers jours, pas pour un logement long terme. Les tarifs mensuels Airbnb sont 40 à 80% plus chers qu'une location directe.</li>
    </ul>

    <h3>Les groupes Facebook</h3>
    <p>C'est ici que se trouvent les meilleures opportunités, souvent des particuliers sans commission d'agence :</p>
    <ul>
      <li>"Hanoi Expats" — le groupe généraliste, section "housing" très active</li>
      <li>"Apartments for rent Hanoi" — dédié au logement, annonces mises à jour quotidiennement</li>
      <li>"Tây Hồ Community" — spécifique au quartier du Lac de l'Ouest</li>
      <li>"French Expats Vietnam" — réseau francophone, pratique pour les recommandations de confiance</li>
    </ul>

    <h3>Les agences immobilières</h3>
    <p>Elles prennent généralement l'équivalent d'un mois de loyer comme commission (à la charge du locataire ou partagée selon les cas). Utile si tu cherches sans être sur place, mais évitable une fois que tu arrives à Hanoï et que tu peux visiter directement.</p>

    <div class="warning-box">
      <strong>⚠️ Arriver avant de signer</strong>
      Évite de signer ou de verser quoi que ce soit avant d'avoir visité en personne. Les photos sur internet sont souvent datées ou retouchées. Réserve un hôtel ou une guesthouse pour tes deux premières semaines, puis cherche ton appartement une fois sur place.
    </div>

    <h2 id="section-visite">La visite : ce qu'il faut vérifier</h2>
    <p>Voici la checklist que j'utilise à chaque visite :</p>
    <ul>
      <li><strong>Climatisation</strong> : teste chaque unité. La clim est indispensable à Hanoï de avril à octobre. Une clim vieille ou mal entretenue multipliera ta facture d'électricité.</li>
      <li><strong>Chauffe-eau</strong> : vérifie qu'il est électrique et fonctionnel. Les douches à l'eau froide ne sont pas une légende.</li>
      <li><strong>Débit internet</strong> : teste en live avec ton téléphone (speedtest). La fibre optique est disponible dans la plupart des immeubles modernes — demande le débit annoncé et le nom du fournisseur.</li>
      <li><strong>Pression d'eau</strong> : ouvre les robinets, tire la chasse d'eau. Les problèmes de pression sont courants dans les vieux bâtiments.</li>
      <li><strong>Bruit</strong> : visite si possible à plusieurs moments de la journée. Les rues de Hanoï peuvent être très bruyantes le matin (klaxons, vendeurs ambulants avec haut-parleurs).</li>
      <li><strong>Compteur électrique</strong> : demande à voir le compteur et note l'index. Certains propriétaires facturent l'électricité sur un compteur partagé.</li>
      <li><strong>Parking scooter</strong> : indispensable si tu as ou prévois d'avoir un scooter. Demande si c'est inclus dans le loyer.</li>
      <li><strong>État général</strong> : moisissures, fissures, fuites sous l'évier — note tout avant de signer et prends des photos horodatées.</li>
    </ul>

    <h2 id="section-contrat">Le contrat de location</h2>
    <p>Exige toujours un contrat écrit, même pour une location informelle. Le contrat idéal est <strong>bilingue vietnamien / anglais ou vietnamien / français</strong> — en cas de litige, c'est la version vietnamienne qui fait foi devant un tribunal vietnamien.</p>

    <p>Ce que le contrat doit mentionner :</p>
    <ul>
      <li>Montant du loyer mensuel et devise (USD ou VND)</li>
      <li>Durée du bail et date d'entrée</li>
      <li>Montant de la caution et conditions de restitution</li>
      <li>Mode de paiement et jour d'échéance</li>
      <li>Qui paie les charges (électricité, eau, internet, service)</li>
      <li>Conditions de résiliation anticipée (préavis, pénalités)</li>
      <li>Inventaire du mobilier et de l'électroménager</li>
    </ul>

    <div class="info-box">
      <strong>📋 Enregistrement du contrat</strong>
      En théorie, les contrats de location à long terme doivent être enregistrés auprès de la mairie (<em>phường</em>) et déclarés aux autorités locales — ce qui implique aussi de déclarer ta présence comme résident étranger. En pratique, peu de propriétaires le font spontanément, mais ça peut t'être demandé pour ton dossier de visa ou de carte de résidence. C'est un point à clarifier avec ton propriétaire avant de signer.
    </div>

