      <div class="faq-answer">Non, ce n'est pas obligatoire. La majorité des loyers à Hanoï se paie en cash (USD ou VND) ou par virement international. Certaines grandes résidences acceptent les cartes. Un compte bancaire vietnamien est pratique mais pas indispensable pour louer.</div>
    </div>

