      <div class="faq-answer">La norme est 6 mois à 1 an. En dessous de 6 mois, on entre dans la catégorie "serviced apartment" avec des tarifs mensuels nettement plus élevés. Pour un long séjour, un contrat de 12 mois avec option de renouvellement est le mieux négocié.</div>
