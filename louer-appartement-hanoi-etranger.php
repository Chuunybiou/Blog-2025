    <h2 id="section-marche">Le marché locatif hanoïen en 2026</h2>
    <p>Hanoï n'est plus la ville bon marché d'il y a dix ans. Le marché locatif a fortement progressé depuis 2022-2023, porté par le retour des expatriés après le Covid et l'afflux de travailleurs des zones industrielles voisines. Dans les quartiers expats comme Tây Hồ, les loyers ont augmenté de 25 à 40% sur les quatre dernières années.</p>
    <p>Deux segments coexistent : les appartements "locaux" (meublés sobrement, propriétaire souvent non-anglophone, loyer en VND) et les appartements "expat" ou "serviced" (meublés haut de gamme, propriétaire habitué aux étrangers, loyer en USD). Pour un premier logement, les appartements expat sont plus simples à gérer, même s'ils coûtent plus cher.</p>
    <div class="info-box">
      <strong>📌 En VND ou en USD ?</strong>
      Les prix dans cet article sont en VND (đồng vietnamien). Repère rapide : <strong>1 € ≈ 27 000 VND</strong> en mai 2026, soit 1 M₫ (million de dong) ≈ 37 €. Certains propriétaires du quartier Tây Hồ affichent encore en USD — dans ce cas, vérifiez toujours à quel taux ils convertissent avant de signer.
    </div>

    <h2 id="section-quartiers">Prix réels par quartier en 2026</h2>
    <p>À Hanoï, il existe <strong>deux marchés locatifs parallèles</strong> qui ne se voient presque jamais sur les mêmes plateformes — et les prix n'ont rien à voir. Les montants sont en VND (millions de dong, noté <strong>M₫</strong>). Rappel : <strong>1 M₫ ≈ 37 €</strong> en mai 2026.</p>

    <div class="info-box">
      <strong>🏙️ Marché "expat" vs marché "local" : la différence clé</strong>
      <p style="margin:0.5rem 0 0">Le marché expat, c'est ce qu'on trouve sur internet : appartements meublés haut de gamme, propriétaires habitués aux étrangers, contrat en anglais possible, eau chaude et clim garanties. C'est pratique pour un primo-arrivant, mais ça se paye.<br><br>
      Le marché local, c'est ce qu'on trouve en marchant dans les rues et en cherchant des panneaux <em>Cho thuê</em> (À louer) : immeubles vietnamiens, propriétaires non-anglophones, contrat en vietnamien, mobilier simple. Les prix sont 30 à 50% moins chers — mais ça demande d'être sur place, de parler un peu vietnamien ou d'avoir un contact local.</p>
    </div>

