    <p><strong>Personne ne m'avait dit que se marier avec une Vietnamienne, c'était potentiellement gérer deux mariages, deux familles, deux pays, deux cultures de la dépense festive — et deux budgets.</strong> Cet article, c'est ce que j'aurais voulu lire avant de me lancer. Des chiffres réels, les postes de dépenses qui surprennent, et ce qu'on aurait fait différemment.</p>

    <p>Je parle de notre expérience. Je ne prétends pas que c'est représentatif de toutes les familles vietnamiennes — les différences régionales, générationnelles et familiales sont immenses. Ce n'est pas "les Vietnamiens font ça" : c'est "voici ce que nous avons vécu".</p>

    <img class="article-photo" src="https://images.unsplash.com/photo-1525772764200-be829a350797?w=1200&q=80" alt="Mariage franco-vietnamien" width="1200" height="675" loading="lazy">

    <h2 id="section-1">Pourquoi un mariage franco-vietnamien coûte souvent deux fois plus cher</h2>
    <p>La structure d'un mariage franco-vietnamien classique implique en général :</p>
    <ul>
      <li><strong>Une cérémonie au Vietnam</strong> — pour la famille de l'épouse qui ne peut pas voyager, pour les traditions familiales, pour la mairie vietnamienne si le mariage civil est célébré là-bas</li>
      <li><strong>Une cérémonie ou réception en France</strong> — pour la famille française, les amis, l'officialisation consulaire</li>
    </ul>
    <p>Ce n'est pas obligatoire. Certains couples font un seul événement, d'un côté ou de l'autre. Mais dans beaucoup de familles vietnamiennes, la présence physique de la famille lors des cérémonies est très importante — et demander à des parents âgés de Hanoï ou de la campagne de prendre l'avion pour Paris n'est pas toujours réaliste.</p>
    <p>Résultat : on se retrouve souvent à planifier deux événements sur deux continents, avec des contraintes logistiques, calendaires et financières bien différentes.</p>

    <h2 id="section-2">Les fiançailles et les cadeaux de la famille</h2>
    <p>C'est le poste qui surprend le plus les Français qui ne l'anticipent pas. Dans la tradition vietnamienne, les fiançailles (<em>đám hỏi</em>) s'accompagnent d'échanges de cadeaux (<em>sính lễ</em>) de la famille du marié vers la famille de la mariée. Ce n'est pas une "dot" au sens patriarcal du terme — c'est un geste de respect et d'honneur entre familles.</p>

    <h3>Ce que ça implique concrètement</h3>
    <p>La forme et le montant varient énormément selon :</p>
    <ul>
      <li>La région (Hanoi, les provinces du Nord, le Sud ont des pratiques différentes)</li>
      <li>La génération des parents — les plus jeunes ont souvent des attentes plus souples</li>
      <li>La situation économique des deux familles</li>
      <li>Ce qui a été discuté et négocié en amont entre les familles</li>
    </ul>
    <div class="info-box">
      <strong>Notre approche :</strong>
      Nous avons eu des discussions ouvertes avec nos deux familles bien en amont. Ma femme a joué un rôle central dans les négociations côté famille — ce qui était à la fois naturel et indispensable pour éviter les malentendus. Ces discussions en amont nous ont évité des surprises et des tensions le jour J.
    </div>
    <p>Dans notre cas, les cadeaux de fiançailles représentaient <strong>550 €</strong> au total (bétel, alcool, gâteaux traditionnels, enveloppe). Ce chiffre n'est pas une règle — il est le résultat de discussions spécifiques à notre situation familiale. Certains couples dépensent dix fois plus, d'autres bien moins.</p>

    <h2 id="section-3">La cérémonie au Vietnam</h2>
    <p>Nous avons célébré notre mariage civil à la mairie de <strong>Ninh Bình</strong> en 2026, suivi d'un repas de famille dans la province. Pas de wedding planner, pas de salle d'hôtel haut de gamme — une cérémonie familiale, dans la région d'origine de sa famille.</p>

    <div class="info-box">
      <strong>L'anecdote des enveloppes :</strong>
      Au Vietnam, les invités offrent des enveloppes rouges en cash (<em>tiền mừng</em>) pendant le repas de noces. On s'attendait à ce que ça couvre une partie des frais. Ce qu'on n'avait pas anticipé : c'est la belle-famille qui centralise et redistribue selon les usages familiaux. Résultat : ça couvre bien les dépenses locales du côté Vietnam, mais pas forcément les frais que vous avez avancés depuis la France. À clarifier en amont avec votre belle-famille pour éviter les malentendus.
    </div>

