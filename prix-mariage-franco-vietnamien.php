    <p>Notre total côté Vietnam hors frais administratifs français : <strong>autour de 142M₫ + 1 100 € + voyages</strong>. En euros consolidés, notre mariage complet nous a coûté environ <strong>7 950 €</strong>.</p>

    <h2 id="section-4">Les frais administratifs : mariage civil et transcription</h2>
    <p>C'est le côté ingrat mais obligatoire. Pour qu'un mariage célébré au Vietnam soit reconnu en France, il faut le faire transcrire par l'ambassade ou le consulat français.</p>

    <h3>Le processus en bref</h3>
    <ol>
      <li>Mariage civil à la mairie vietnamienne (UBND) — gratuit ou frais administratifs minimes</li>
      <li>Obtention de l'acte de mariage vietnamien — traduit par traducteur assermenté</li>
      <li>Dépôt du dossier de transcription au consulat français de Hanoï ou Ho Chi Minh-Ville</li>
      <li>Délai de traitement : 3 à 8 semaines selon les périodes</li>
    </ol>
    <div class="tip-box">
      <strong>💡 La transcription est gratuite :</strong>
      La transcription de l'acte de mariage au registre consulaire français ne coûte rien en droits. Les frais réels sont ceux des traductions et du dossier administratif. Dans notre cas, les frais de papiers côté France s'élevaient à <strong>50 €</strong> environ. Notre article sur <a href="se-marier-vietnamienne-demarches-france.php">les démarches de mariage côté France</a> détaille tout le dossier.
    </div>
    <p>Budget administratif France : <strong>~50 €</strong> dans notre cas. À quoi s'ajoutent les frais locaux au Vietnam (~8 € de frais informels d'accélération), négligeables mais réels.</p>

    <h2 id="section-5">La réception en France</h2>
    <p>Nous avons organisé un repas de famille en France quelques mois après le Vietnam, pour la famille française qui n'avait pas pu faire le déplacement. Un repas de mariage classique — mais dimensionné modestement.</p>
    <p>Pour 40 personnes, dans un restaurant privatisé en région parisienne, sans orchestre ni wedding planner :</p>
    <ul>
      <li>Location de salle / privatisation restaurant : 500 – 1 500 €</li>
      <li>Repas (menu fixe, boissons comprises) : 60 – 120 € par personne</li>
      <li>Gâteau de mariage : 200 – 600 €</li>
      <li>Photographe : 800 – 2 000 €</li>
      <li>Décoration, papeterie, imprévus : 300 – 800 €</li>
    </ul>
    <p>Pour 40 personnes, comptez <strong>5 000 – 9 000 €</strong> pour une réception correcte sans excès.</p>

