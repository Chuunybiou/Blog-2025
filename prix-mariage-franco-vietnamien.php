      Les enveloppes rouges données aux invités (<em>tiền mừng</em>), les cadeaux pour les oncles et tantes proches, les achats de dernière minute à Hanoï… Ces petits postes s'accumulent vite. Je conseille de prévoir une enveloppe "divers" de 10 à 15 % du budget total.
    </div>

    <h2 id="section-7">Ce qu'on aurait fait différemment</h2>
    <p>Avec le recul, voici ce que je changerais :</p>
    <ul>
      <li><strong>Discuter du budget bien en amont avec les deux familles.</strong> Pas à J-15. Plusieurs mois avant. Les attentes s'ajustent plus facilement quand on les pose tôt, sans pression de l'événement imminent.</li>
      <li><strong>Ne pas sous-estimer les billets d'avion.</strong> Si des membres de la famille française veulent assister à la cérémonie au Vietnam, c'est un budget conséquent à intégrer dès le départ — et à anticiper pour les réserver longtemps à l'avance.</li>
      <li><strong>Faire confiance à ma femme pour les négociations familiales côté Vietnam.</strong> Elle connaît les codes, les attentes implicites et les personnes à impliquer. Mon rôle était d'être présent et respectueux, pas de piloter.</li>
      <li><strong>Prévoir un compte dédié au mariage.</strong> Un sous-compte Wise ou Revolut spécifique au budget mariage simplifie le suivi et évite de mélanger les dépenses courantes avec les dépenses exceptionnelles.</li>
    </ul>

