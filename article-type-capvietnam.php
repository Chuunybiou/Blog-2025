    <h2 id="section-6">Mon expérience personnelle</h2>
    <p>Dans mon cas, voici mon plan : j'arrive d'abord avec un <strong>e-visa de 90 jours</strong> pour m'installer, trouver un appartement à Hanoï et gérer les premières démarches. Pendant ce temps, on prépare le dossier de mariage avec ma copine.</p>
