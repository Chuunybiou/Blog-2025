    <p><strong>Tu veux t'installer au Vietnam mais tu ne sais pas quel visa choisir ?</strong> Normal — entre l'e-visa, le visa de travail, le visa de regroupement familial et les multiples sous-catégories, c'est un vrai casse-tête. Dans cet article, je te résume tout ce que j'ai appris en préparant ma propre expatriation.</p>

    <div class="article-img">🇻🇳</div>

    <h2 id="section-1">Les différents types de visa pour le Vietnam</h2>
    <p>Le Vietnam propose plusieurs catégories de visa aux ressortissants français. Le bon choix dépend de ta situation : tu viens en touriste, pour travailler, ou pour rejoindre ton conjoint vietnamien.</p>
    <p>Voici les trois principales options que je vais détailler dans cet article :</p>
    <ul>
      <li><strong>L'e-visa</strong> — pour les séjours de moins de 90 jours (tourisme, repérage)</li>
      <li><strong>Le visa de travail (DN/LD)</strong> — si tu as un employeur ou crées une entreprise</li>
      <li><strong>Le visa de regroupement familial (TT)</strong> — si ton conjoint est vietnamien</li>
    </ul>

    <h2 id="section-2">L'e-visa : simple et rapide</h2>
    <p>L'e-visa est le plus facile à obtenir. Tu fais la demande en ligne sur le portail officiel du gouvernement vietnamien, et tu reçois ta réponse par email en 3 jours ouvrés.</p>
    <div class="info-box">
      <strong>📋 Ce qu'il te faut pour l'e-visa :</strong>
      Un passeport valide 6 mois après la date d'entrée, une photo d'identité numérique, une carte bancaire pour le paiement (25 USD), et ton itinéraire de voyage.
    </div>
    <p>L'e-visa est valable <strong>90 jours</strong>, entrées multiples. C'est parfait pour un séjour de repérage : tu visites, tu testes la vie sur place, et tu prépares la suite.</p>
    <div class="warning-box">
      <strong>⚠️ Attention :</strong>
      L'e-visa ne te permet PAS de travailler au Vietnam, même en freelance. Techniquement, travailler en ligne depuis le Vietnam avec un e-visa est dans une zone grise juridique. Si tu veux être 100% en règle, tu devras passer à un autre type de visa.
    </div>

    <h2 id="section-3">Le visa de travail : pour travailler légalement</h2>
    <p>Si tu as un employeur au Vietnam ou si tu crées une entreprise sur place, tu auras besoin d'un visa de travail. C'est le visa <strong>DN</strong> (business) ou <strong>LD</strong> (travailleur), accompagné d'un permis de travail (<em>work permit</em>).</p>
    <h3>Les étapes pour obtenir un visa de travail</h3>
    <ol>
      <li>Ton employeur au Vietnam fait une demande de permis de travail auprès du Département du Travail</li>
      <li>Tu fournis un extrait de casier judiciaire, un certificat médical, et tes diplômes (apostillés et traduits)</li>
      <li>Le permis de travail est délivré (durée max : 2 ans)</li>
      <li>Avec le permis, tu demandes le visa de travail à l'ambassade du Vietnam ou au bureau d'immigration</li>
    </ol>
    <div class="tip-box">
      <strong>💡 Mon conseil :</strong>
      Les démarches pour le permis de travail prennent 2 à 4 semaines. Commence à préparer tes documents (casier judiciaire, apostille, traductions assermentées) dès maintenant — c'est la partie la plus longue.
    </div>

    <h2 id="section-4">Le visa de regroupement familial (TT)</h2>
    <p>C'est le visa qui me concerne directement. Si ta femme est vietnamienne et que vous êtes mariés, tu peux obtenir un <strong>visa TT</strong> (thăm thân — visite familiale) qui te permet de résider au Vietnam.</p>
    <p>Ce visa est valable jusqu'à <strong>5 ans</strong> et peut être renouvelé. C'est souvent la solution la plus simple pour les couples franco-vietnamiens.</p>
    <h3>Documents nécessaires</h3>
    <ul>
      <li>Acte de mariage transcrit (reconnu au Vietnam)</li>
      <li>Lettre d'invitation de ta femme (formulaire NA5)</li>
      <li>Copie du livret de famille vietnamien (<em>sổ hộ khẩu</em>)</li>
      <li>Passeport valide 6 mois</li>
      <li>2 photos d'identité</li>
    </ul>
    <div class="info-box">
      <strong>📋 Bon à savoir :</strong>
      Avec un visa TT, tu ne peux pas travailler non plus. Mais tu peux ensuite demander une carte de résidence temporaire (<em>thẻ tạm trú</em>) qui te donne plus de droits et de stabilité. La carte de résidence est valable de 1 à 5 ans.
    </div>

