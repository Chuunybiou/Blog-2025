    <p><strong>L'argent est l'un des sujets les plus sensibles dans n'importe quel couple.</strong> Dans un couple franco-vietnamien, il y a souvent une couche supplémentaire : deux contextes économiques différents, deux façons d'en parler (ou de ne pas en parler), et parfois des attentes familiales qui entrent dans l'équation.</p>

    <p>Je vais être direct dans cet article. Pas pour généraliser sur les Vietnamiens ou sur les Français — mais pour partager ce que nous avons traversé, les erreurs qu'on a faites et comment on a construit quelque chose qui fonctionne pour nous.</p>

    <div class="warning-box">
      <strong>Note importante :</strong>
      Tout ce que j'écris ici reflète mon expérience personnelle avec ma femme et nos deux familles. Il serait absurde d'en faire une règle universelle. Chaque couple est différent. Chaque famille est différente. Lisez ceci comme un témoignage, pas comme un guide culturel.
    </div>

    <img class="article-photo" src="https://images.unsplash.com/photo-1579621970563-ebec7560ff3e?w=1200&q=80" alt="Budget et finances couple expatrié" width="1200" height="675" loading="lazy">

    <h2 id="section-1">Le sujet qu'on évite — et pourquoi c'est une erreur</h2>
    <p>Quand on est au début d'une relation et que tout va bien, on n'a pas envie de parler d'argent. C'est casse-pieds, ça fait sérieux, ça peut créer des tensions là où il n'y en a pas encore. Alors on reporte. Et on reporte encore.</p>
    <p>Dans un couple franco-vietnamien, cette tendance est souvent amplifiée par le fait que les deux partenaires évitent de "froisser" l'autre en abordant des sujets potentiellement culturellement sensibles. Lui ne veut pas avoir l'air de compter. Elle ne veut pas paraître intéressée. Résultat : personne ne parle de rien, et les non-dits s'accumulent.</p>
    <p>Notre premier vrai clash sur l'argent est venu d'une incompréhension que six mois de conversation ouverte en amont auraient pu éviter. Je ne détaillerai pas les circonstances par respect pour notre vie privée — mais la leçon est simple : parler tôt vaut mieux que gérer une crise tard.</p>

    <h2 id="section-2">Deux rapports à l'argent, pas forcément opposés</h2>
    <p>Je vais faire attention à ne pas tomber dans les clichés. Les différences que j'observe dans notre couple ne sont pas "françaises vs. vietnamiennes" — elles sont personnelles, familiales, et liées à nos histoires respectives.</p>
    <p>Cela dit, certaines choses m'ont surpris dans les premières années :</p>
    <ul>
      <li><strong>La notion de "gagner de l'argent ensemble"</strong> — dans ma culture familiale, l'argent du couple est séparé jusqu'au mariage, puis progressivement mis en commun. Dans sa famille à elle, la mise en commun était plus rapide, plus naturelle dès que la relation est sérieuse.</li>
      <li><strong>L'argent liquide</strong> — ma femme préfère le cash pour beaucoup de transactions. J'ai grandi avec la carte partout. Ce n'est pas un problème, c'est une habitude différente qui demande une organisation commune.</li>
      <li><strong>Épargner vs. dépenser</strong> — j'avais l'image (stéréotypée et fausse) que les familles vietnamiennes dépensent beaucoup lors des événements familiaux. Ce que j'ai vu dans notre famille, c'est une épargne rigoureuse au quotidien pour pouvoir dépenser généreusement lors des moments importants. Une logique tout à fait cohérente.</li>
    </ul>
    <p>Ces différences ne sont ni bonnes ni mauvaises. Elles demandent d'en parler et de construire une logique commune.</p>

    <h2 id="section-3">Le soutien financier à la famille vietnamienne</h2>
    <p>C'est le sujet qui revient le plus souvent dans les discussions des couples franco-vietnamiens que je connais. Et celui qui crée le plus de tensions quand il n'est pas traité ouvertement.</p>
    <p>Dans de nombreuses familles vietnamiennes, les enfants adultes contribuent financièrement aux parents, surtout quand les parents vieillissent ou n'ont pas de retraite suffisante. Ce n'est pas une "demande excessive" — c'est une norme familiale et un marqueur de gratitude et d'appartenance. C'est souvent quelque chose auquel ma femme tient profondément.</p>
    <div class="info-box">
      <strong>Ce que j'ai appris à faire :</strong>
      Traiter ce soutien comme un poste budgétaire à part entière — au même titre que notre loyer ou notre assurance. Pas comme une dépense imprévue ou une faveur ponctuelle. Une fois intégré dans notre budget mensuel, il n'est plus source de tension : c'est une ligne budgétaire comme une autre, planifiée et assumée.
    </div>
    <p>Côté pratique, les transferts vers le Vietnam se font via Wise : rapides, peu coûteux, et sa famille peut retirer l'argent facilement à l'agence ou le recevoir sur son compte Vietcombank.</p>
    <p>Ce qui est important, c'est la transparence dans les deux sens. Je lui parle aussi des dépenses que j'engage pour ma famille en France. Ce n'est pas "ton argent et le mien" — c'est notre budget familial, avec des engagements vers les deux familles.</p>

    <h2 id="section-4">Notre organisation des dépenses communes</h2>
    <p>Après plusieurs ajustements, voici comment nous fonctionnons aujourd'hui. Ce modèle s'est construit progressivement — il n'est pas venu d'un coup.</p>

    <h3>La règle des 70 %</h3>
    <p>Notre organisation repose sur un principe simple : <strong>70 % des dépenses d'un couple franco-vietnamien vont vers la famille</strong>, pas vers le couple lui-même. Ce n'est pas une critique — c'est une réalité à intégrer dès le départ. Les fêtes, les cérémonies, les cadeaux, les contributions aux parents, les voyages en famille... tout ça représente une part bien plus importante du budget que dans un couple franco-français.</p>
    <ul>
      <li><strong>Dépenses communes du foyer</strong> — loyer, courses, transports, abonnements. Un pot commun alimenté par chacun selon les revenus du moment.</li>
      <li><strong>Dépenses personnelles</strong> — chacun garde son autonomie. Pas de compte à rendre sur ses propres dépenses.</li>
      <li><strong>Soutien aux familles respectives</strong> — budgété à l'avance, décidé ensemble, traité de façon transparente des deux côtés.</li>
    </ul>
    <p>Le pot commun passe par un compte Wise. Les dépenses personnelles restent sur nos comptes individuels (son Vietcombank, mon Boursorama).</p>

