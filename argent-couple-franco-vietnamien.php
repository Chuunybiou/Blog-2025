    <h2 id="section-5">Construire la transparence financière : comment on y est arrivés</h2>
    <p>Ça n'a pas été immédiat. La difficulté n'était pas technique — c'était d'oser poser les sujets clairement, sans peur d'offenser.</p>

    <h3>1. Parler avant le mariage, pas après</h3>
    <p>Mon conseil le plus important est celui-ci : <strong>avant de vous marier, mettez sur la table toutes vos règles concernant l'argent</strong>. Ce que chacun envoie à sa famille, comment vous partagez les dépenses du foyer, ce qui appartient à qui en cas de séparation. Ce n'est pas romantique. C'est indispensable.</p>
    <p>Les couples qui évitent ces conversations avant le mariage se retrouvent à les avoir dans des moments de tension, quand les enjeux sont bien plus élevés et les esprits bien moins disponibles.</p>

    <h3>2. Le concept de <em>giữ thể diện</em> — et ce que j'aurais dû comprendre plus tôt</h3>
    <p>La notion de "garder la face" (<em>giữ thể diện</em>) joue un rôle dans beaucoup de décisions financières côté famille vietnamienne — les cadeaux à faire lors des événements, les contributions visibles lors des cérémonies, la générosité publique comme marqueur de statut. Je comprends mieux maintenant pourquoi certaines dépenses me paraissaient irrationnelles : elles avaient une logique sociale que je ne voyais pas.</p>
    <p>Je ne qualifie pas ça de bon ou mauvais. C'est un système différent du mien, avec sa cohérence interne. Le comprendre m'a évité beaucoup de malentendus.</p>

    <h3>3. Les cadeaux de produits français : un coût réel à anticiper</h3>
    <p>À chaque aller-retour depuis la France, je ramène des produits français pour la famille : fromages, vins, chocolats, cosmétiques. Ça représente entre <strong>100 et 300 € par voyage</strong> selon les circonstances et les demandes. C'est devenu une ligne budgétaire à part entière dans nos déplacements — pas un surcoût imprévu.</p>

    <h3>4. Le "compte illimité" : un malentendu à désamorcer</h3>
    <p>Il m'a fallu du temps pour comprendre que, dans la perception de certains membres de sa famille, un Français en couple avec une Vietnamienne = quelqu'un qui a un "compte en banque illimité". Ce n'est pas malveillant — c'est une représentation construite par la différence de niveau de vie perçue. La réponse n'est pas la défense ni la culpabilité : c'est la clarté. Être précis sur ce qu'on peut faire et ce qu'on ne peut pas faire, sans gêne.</p>

    <h2 id="section-6">Les signaux d'alerte dans un couple mixte</h2>
    <p>Voici des situations qui méritent une conversation ouverte, pas une solution unilatérale :</p>
    <ul>
      <li>Une des deux personnes se sent contrainte de cacher des dépenses à l'autre</li>
      <li>Le soutien à une famille extérieure crée un déséquilibre qui pèse sur le couple</li>
      <li>L'un des partenaires a une vision radicalement différente de l'autre sur ce que signifie "épargner"</li>
      <li>Les décisions financières importantes sont prises sans discussion préalable</li>
    </ul>
    <p>Aucun de ces signaux n'est spécifique aux couples franco-vietnamiens. Mais dans un contexte interculturel, ils peuvent être plus difficiles à nommer parce qu'on a peur d'être perçu comme "pas assez ouvert culturellement".</p>
    <div class="tip-box">
      <strong>💡 Ce que j'ai retenu :</strong>
      La culture est un contexte, pas une excuse. Si quelque chose me dérange financièrement dans notre couple, c'est ma responsabilité de le dire clairement — et la sienne de l'entendre. L'inverse aussi. "C'est culturel" ne met pas fin à une conversation : ça la commence.
    </div>

