    <h2 id="section-1">Pourquoi une assurance privée est indispensable</h2>
    <p>La Sécurité sociale française ne couvre pas les soins à l'étranger sauf cas spécifiques (détachement, accord bilatéral). Le Vietnam et la France n'ont pas de convention de sécurité sociale bilatérale couvrant les soins courants. Si tu travailles à distance ou que tu es indépendant au Vietnam, tu pars donc sans couverture de base — et les urgences médicales peuvent coûter plusieurs milliers de dollars dans un hôpital international.</p>

    <div class="warning-box">
      <strong>⚠️ Rapatriement sanitaire :</strong>
      Une hospitalisation sérieuse suivie d'un rapatriement médical vers la France peut coûter entre 20 000 et 100 000 €. Sans assurance incluant cette garantie, tu paies de ta poche. C'est la garantie à ne jamais négliger.
    </div>

    <h2 id="section-2">L'assurance sociale vietnamienne (Bảo hiểm xã hội)</h2>
    <p>Si tu travailles au Vietnam avec un contrat local (permis de travail + contrat de droit vietnamien), tu es en principe <strong>obligatoirement affilié à la sécurité sociale vietnamienne</strong> (Loi sur l'assurance sociale n°58/2014/QH13). Les cotisations sont prélevées automatiquement :</p>
    <ul>
      <li>Part employeur : 17,5% du salaire brut (assurance sociale + santé + chômage)</li>
      <li>Part salarié : 10,5% du salaire brut</li>
    </ul>
    <p>La couverture est réelle mais limitée : soins dans les hôpitaux publics, remboursements partiels sur liste positive, pas de médecins francophones ni anglophones dans les hôpitaux conventionnés. Pour les expatriés, c'est un filet de sécurité insuffisant seul.</p>

    <div class="info-box">
      <strong>📋 Depuis 2024 :</strong>
      La loi sur l'assurance santé (sửa đổi Luật BHYT 2023) a étendu les obligations d'affiliation aux étrangers titulaires d'une carte de résidence temporaire de plus d'un an. Renseigne-toi auprès de ton employeur.
    </div>

