    <div class="warning-box">
      <strong>Dengue : pas de vaccin préventif standard :</strong>
      Il n'existe pas de vaccin dengue recommandé en prophylaxie générale pour les voyageurs en 2026. La protection passe uniquement par la prévention anti-moustiques : répulsif avec DEET ≥20%, vêtements couvrants au crépuscule, moustiquaire imprégnée. La dengue peut entraîner une hospitalisation de plusieurs jours et des complications graves.
    </div>

    <h2 id="section-8">La médecine traditionnelle vietnamienne</h2>
    <p>La <em>y học cổ truyền</em> (médecine traditionnelle vietnamienne) est une médecine reconnue et encadrée par l'État. Elle cohabite avec la médecine occidentale dans le système de santé public et dans de nombreuses cliniques privées.</p>
    <p>Les pratiques les plus courantes au Vietnam :</p>
    <ul>
      <li><strong>Acupuncture (châm cứu)</strong> : très répandue et remboursée par l'assurance sociale dans les hôpitaux publics. Efficace et peu onéreuse pour les douleurs chroniques (50 000–200 000 VND la séance).</li>
      <li><strong>Ventouses (giác hơi)</strong> : utilisées dans les familles pour les rhumes et les douleurs musculaires. Très courante en automédication domestique.</li>
      <li><strong>Herboristerie (thuốc nam)</strong> : pharmacies traditionnelles nombreuses dans les vieux quartiers de Hanoï, notamment Phố Thuốc Bắc (rue des médicaments).</li>
      <li><strong>Gua sha (cạo gió)</strong> : grattage de la peau avec un outil pour "chasser le vent du corps". Laisse des marques rouges inoffensives mais peut surprendre un médecin occidental non prévenu.</li>
    </ul>
    <div class="info-box">
      <strong>Hôpital de référence :</strong>
      L'Hôpital de Médecine Traditionnelle de Hanoï (Bệnh viện Y học cổ truyền Hà Nội), 29 Nguyễn Bỉnh Khiêm, Ba Đình, est l'établissement public de référence. Consultations de 50 000 à 200 000 VND. Certaines mutuelles expatriées remboursent l'acupuncture comme médecine complémentaire — vérifier les conditions générales du contrat.
    </div>

