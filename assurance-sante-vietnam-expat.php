    <h2 id="section-4">Les hôpitaux internationaux à Hanoï</h2>
    <p>Ces établissements accueillent les expatriés avec des médecins anglophones ou francophones et des standards proches des normes européennes :</p>
    <ul>
      <li><strong>Hôpital Vinmec Times City</strong> — standard international, nombreuses spécialités, urgences 24h/24</li>
      <li><strong>Hôpital Français de Hanoï (Hôpital Saint-Paul réhabilité)</strong> — médecins francophones disponibles certains jours</li>
      <li><strong>Family Medical Practice Hanoi</strong> — clinique internationale très fréquentée par les expats, médecins occidentaux</li>
      <li><strong>Hong Ngoc Hospital</strong> — bon rapport qualité/prix pour les soins courants</li>
    </ul>

    <h2 id="section-5">Les coûts réels à anticiper</h2>
    <ul>
      <li>Consultation généraliste en clinique internationale : 50–100 USD</li>
      <li>Consultation spécialiste : 100–200 USD</li>
      <li>Nuit en hospitalisation hôpital international : 300–600 USD</li>
      <li>Radiographie / scanner : 80–250 USD</li>
      <li>Rapatriement médical vers la France : 20 000–80 000 €</li>
    </ul>

    <h2 id="section-6">La CFE en détail : conditions et fonctionnement</h2>
    <p>La <strong>Caisse des Français de l'Étranger (CFE)</strong> est une adhésion volontaire à l'assurance maladie française depuis l'étranger. Elle est souvent mal comprise mais peut être la solution la plus avantageuse pour les Français au Vietnam sur le long terme.</p>
    <p>Fonctionnement : tu cotises à la CFE et bénéficies des remboursements du régime général français, selon les tarifs conventionnels. Les soins effectués au Vietnam sont remboursés sur la base des tarifs de référence français — ce qui couvre partiellement les consultations dans les cliniques internationales de Hanoï.</p>
    <ul>
      <li><strong>Cotisation mensuelle</strong> : environ 70–150 €/mois selon l'âge et la catégorie choisie (1 à 3)</li>
      <li><strong>Conditions d'éligibilité</strong> : être de nationalité française, résider à l'étranger, ne pas être affilié à un régime obligatoire étranger incompatible</li>
      <li><strong>Ce qui est couvert</strong> : hospitalisation, consultations, maternité, invalidité, accidents du travail (option)</li>
      <li><strong>Ce qui ne l'est pas</strong> : soins dentaires et optiques sans complémentaire, dépassements d'honoraires non pris en charge à 100%</li>
    </ul>
    <div class="tip-box">
      <strong>Combinaison optimale :</strong>
      CFE (base) + mutuelle complémentaire adaptée à l'Asie = couverture complète pour environ 130–200 €/mois. Moins cher qu'une mutuelle expatriée seule haut de gamme, et tu maintiens tes droits Sécurité sociale française pour ton retour.
    </div>

    <h2 id="section-7">Vaccinations recommandées avant et pendant le séjour</h2>
    <p>Le Vietnam est en zone tropicale. Certaines maladies absentes en France circulent activement. Le Ministère français des Affaires étrangères (Conseils aux Voyageurs) recommande les vaccinations suivantes pour un séjour au Vietnam :</p>
